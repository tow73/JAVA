
public class Phone {
	private String name;
	private String tel;
	
	public Phone(String n, String t) {
		name = n;
		tel = t;
	}
	
	public String getName() { return name; }
	public void setName(String n) { name = n; }
	public String getTel() { return tel; }
	public void setTel(String t) { tel = t; }
}
