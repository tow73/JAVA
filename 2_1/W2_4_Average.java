
public class Average {
	public int getAverage(int a, int b) {
		int sum = 0;
		int avg = 0;
		
		sum = a + b;
		avg = sum / 2;
		
		return avg;
	}
	public int getAverage(int a, int b, int c) {
		int sum = 0;
		int avg = 0;
		
		sum = a + b + c;
		avg = sum / 3;
		
		return avg;
	}
}
